package emanuellynogueiratrizotti.model;

public class Mesa {
    private int idMesa;
    private int numero;
    private String statusOcupacao;

    public int getIdMesa() { return idMesa; }
    public void setIdMesa(int idMesa) { this.idMesa = idMesa; }
    public int getNumero() { return numero; }
    public void setNumero(int numero) { this.numero = numero; }
    public String getStatusOcupacao() { return statusOcupacao; }
    public void setStatusOcupacao(String statusOcupacao) { this.statusOcupacao = statusOcupacao; }
}