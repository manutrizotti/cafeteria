    private void preencherCombosItens() {
        cmbProdutoItem.removeAllItems();
        cmbCozinheiroItem.removeAllItems();
        
        cmbProdutoItem.addItem("Selecione");
        cmbCozinheiroItem.addItem("Selecione");

        // 1. Preenche os Produtos
        emanuellynogueiratrizotti.dao.ProdutoDAO prodDao = new emanuellynogueiratrizotti.dao.ProdutoDAO();
        java.util.List<emanuellynogueiratrizotti.model.Produto> produtos = prodDao.listarTodos();
        for (emanuellynogueiratrizotti.model.Produto p : produtos) {
            cmbProdutoItem.addItem(p.getIdProduto() + " - " + p.getNome());
        }

        // 2. Preenche apenas os Cozinheiros
        emanuellynogueiratrizotti.dao.FuncionarioDAO funcDao = new emanuellynogueiratrizotti.dao.FuncionarioDAO();
        java.util.List<emanuellynogueiratrizotti.model.Funcionario> funcionarios = funcDao.listarTodos();
        for (emanuellynogueiratrizotti.model.Funcionario f : funcionarios) {
            if (f.getCargo().equals("Cozinheiro")) {
                cmbCozinheiroItem.addItem(f.getIdFuncionario() + " - " + f.getNome());
            }
        }
    }

    private void carregarTabelaItens() {
        emanuellynogueiratrizotti.dao.ItemDAO dao = new emanuellynogueiratrizotti.dao.ItemDAO();
        java.util.List<emanuellynogueiratrizotti.model.Item> lista = dao.listarPorComanda(this.idComandaAtual);

        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblItens.getModel();
        modelo.setRowCount(0);

        for (emanuellynogueiratrizotti.model.Item i : lista) {
            modelo.addRow(new Object[]{
                i.getIdProduto(),       // Coluna 0: Continua guardando o ID (essencial para o botão Excluir funcionar!)
                i.getNomeProduto(),     // Coluna 1: Agora exibe o TEXTO do produto para o usuário
                i.getQtd(),             // Coluna 2: Quantidade
                i.getNomeCozinheiro()   // Coluna 3: Agora exibe o TEXTO do funcionário
            });
        }
        // 1. Instancia o DAO da comanda para acessar a função matemática
    emanuellynogueiratrizotti.dao.ComandaDAO comandaDao = new emanuellynogueiratrizotti.dao.ComandaDAO();
    
    // 2. Puxa o total do banco (passando false para não embutir a gorjeta nesta tela de itens)
    double valorTotal = comandaDao.calcularTotal(this.idComandaAtual, false);
    
    // 3. Formata o número normal para o padrão de moeda do Brasil (ex: 15.5 vira 15,50)
    lblTotalComanda.setText(String.format("Total: R$ %.2f", valorTotal));
    }
