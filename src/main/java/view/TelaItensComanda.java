    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
if (cmbProdutoItem.getSelectedIndex() == 0 || cmbCozinheiroItem.getSelectedIndex() == 0 || txtQuantidadeItem.getText().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Preencha todos os campos para adicionar o item!");
        return;
    }

    try {
        emanuellynogueiratrizotti.model.Item i = new emanuellynogueiratrizotti.model.Item();
        
        // O ID da comanda já está salvo na memória da tela!
        i.setIdComanda(this.idComandaAtual);
        
        i.setIdProduto(Integer.parseInt(cmbProdutoItem.getSelectedItem().toString().split(" - ")[0]));
        i.setQtd(Integer.parseInt(txtQuantidadeItem.getText()));
        i.setIdFuncionarioCozinheiro(Integer.parseInt(cmbCozinheiroItem.getSelectedItem().toString().split(" - ")[0]));

        emanuellynogueiratrizotti.dao.ItemDAO dao = new emanuellynogueiratrizotti.dao.ItemDAO();
        dao.adicionarItem(i);

        txtQuantidadeItem.setText("");
        cmbProdutoItem.setSelectedIndex(0);
        carregarTabelaItens();
        
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "A quantidade deve ser um número inteiro válido.");
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Erro ao adicionar item: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
int linha = tblItens.getSelectedRow();
    
    if (linha == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Selecione um item na tabela para remover!");
        return;
    }

    int op = javax.swing.JOptionPane.showConfirmDialog(this, "Deseja remover este item do pedido?", "Confirmação", javax.swing.JOptionPane.YES_NO_OPTION);
    
    if (op == javax.swing.JOptionPane.YES_OPTION) {
        int idProduto = (int) tblItens.getValueAt(linha, 0);
        
        emanuellynogueiratrizotti.dao.ItemDAO dao = new emanuellynogueiratrizotti.dao.ItemDAO();
        dao.removerItem(this.idComandaAtual, idProduto);

        carregarTabelaItens();
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tblItensMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblItensMouseClicked
int linha = tblItens.getSelectedRow();
    if (linha != -1) {
        // 1. Pega o ID do Produto da tabela (Coluna 0) e procura no JComboBox
        String idProduto = tblItens.getValueAt(linha, 0).toString();
        for (int i = 0; i < cmbProdutoItem.getItemCount(); i++) {
            if (cmbProdutoItem.getItemAt(i).startsWith(idProduto + " - ")) {
                cmbProdutoItem.setSelectedIndex(i);
                break;
            }
        }
        
        // 2. Joga a quantidade de volta para o campo de texto (Coluna 1)
        txtQuantidadeItem.setText(tblItens.getValueAt(linha, 1).toString());
        
        // 3. Pega o ID do Cozinheiro da tabela (Coluna 2) e procura no JComboBox
        String idCozinheiro = tblItens.getValueAt(linha, 2).toString();
        for (int i = 0; i < cmbCozinheiroItem.getItemCount(); i++) {
            if (cmbCozinheiroItem.getItemAt(i).startsWith(idCozinheiro + " - ")) {
                cmbCozinheiroItem.setSelectedIndex(i);
                break;
            }
        }
    }
    }//GEN-LAST:event_tblItensMouseClicked

