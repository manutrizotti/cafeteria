    private void tblComandasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblComandasMouseClicked
int linha = tblComandas.getSelectedRow();
    if (linha == -1) return;

    try {
        int idComanda = (int) tblComandas.getValueAt(linha, 0);
        
        int idMesa = 0;
        int numeroMesa = 0; // Nova variável para guardar o número da mesa
        int idGarcom = 0;
        int idCaixa = 0;
        String status = "";
        String pgto = "";
        double gorjeta = 0.0;
        
        // Fazemos um JOIN com a tabela mesas para descobrir o número real da mesa
        String sql = "SELECT c.id_mesa, m.numero as numero_mesa, c.status, c.forma_pgto, c.gorjeta, c.id_funcionario_garcom, c.id_funcionario_caixa " +
                     "FROM comandas c JOIN mesas m ON c.id_mesa = m.id_mesa " +
                     "WHERE c.id_comanda = ?";
                     
        try (java.sql.Connection conn = emanuellynogueiratrizotti.util.ConexaoDB.conectar();
             java.sql.PreparedStatement stmt = conn.prepareStatement(sql)) {
            
            stmt.setInt(1, idComanda);
            try (java.sql.ResultSet rs = stmt.executeQuery()) {
                if (rs.next()) {
                    idMesa = rs.getInt("id_mesa");
                    numeroMesa = rs.getInt("numero_mesa"); // Resgata o número
                    status = rs.getString("status");
                    pgto = rs.getString("forma_pgto");
                    gorjeta = rs.getDouble("gorjeta");
                    idGarcom = rs.getInt("id_funcionario_garcom");
                    idCaixa = rs.getInt("id_funcionario_caixa");
                }
            }
        }

        // Localiza a Mesa na Combobox
        boolean mesaEncontrada = false;
        for (int i = 0; i < cmbMesaComanda.getItemCount(); i++) {
            if (cmbMesaComanda.getItemAt(i).startsWith(idMesa + " - ")) {
                cmbMesaComanda.setSelectedIndex(i);
                mesaEncontrada = true;
                break;
            }
        }
        
        // SEGREDO AQUI: Se a mesa está ocupada, ela não está na combobox. Adicionamos ela agora!
        if (!mesaEncontrada && idMesa != 0) {
            String itemMesa = idMesa + " - Mesa " + numeroMesa;
            cmbMesaComanda.addItem(itemMesa);
            cmbMesaComanda.setSelectedItem(itemMesa);
        }

        // Localiza o Garçom
        for (int i = 0; i < cmbGarcomComanda.getItemCount(); i++) {
            if (cmbGarcomComanda.getItemAt(i).startsWith(idGarcom + " - ")) {
                cmbGarcomComanda.setSelectedIndex(i);
                break;
            }
        }

        // Localiza o Caixa
        for (int i = 0; i < cmbCaixaComanda.getItemCount(); i++) {
            if (cmbCaixaComanda.getItemAt(i).startsWith(idCaixa + " - ")) {
                cmbCaixaComanda.setSelectedIndex(i);
                break;
            }
        }

        cmbStatusComanda.setSelectedItem(status);
        if (pgto != null && !pgto.isEmpty()) {
            cmbPgtoComanda.setSelectedItem(pgto);
        } else {
            cmbPgtoComanda.setSelectedIndex(0); 
        }
        txtGorjetaComanda.setText(String.valueOf(gorjeta));

    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Erro ao carregar dados da comanda: " + e.getMessage());
    }
    }//GEN-LAST:event_tblComandasMouseClicked

    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
// Validação de Fechamento
    if (cmbStatusComanda.getSelectedItem().toString().equals("FECHADA") && cmbPgtoComanda.getSelectedIndex() == 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Para fechar a comanda, você deve selecionar a Forma de Pagamento!");
        return;
    }
        
    int linha = tblComandas.getSelectedRow();
    if (linha == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Selecione uma comanda na tabela para atualizar!");
        return;
    }

    try {
        // Resgata a mesa antiga da tabela antes de sobrescrever
        int idMesaAntiga = (int) tblComandas.getValueAt(linha, 1);
        
        emanuellynogueiratrizotti.model.Comanda c = new emanuellynogueiratrizotti.model.Comanda();
        c.setIdComanda((int) tblComandas.getValueAt(linha, 0));
        
        if (cmbMesaComanda.getSelectedIndex() == 0) {
            c.setIdMesa(idMesaAntiga);
        } else {
            c.setIdMesa(Integer.parseInt(cmbMesaComanda.getSelectedItem().toString().split(" - ")[0]));
        }
        
        c.setIdFuncionarioGarcom(Integer.parseInt(cmbGarcomComanda.getSelectedItem().toString().split(" - ")[0]));
        c.setIdFuncionarioCaixa(Integer.parseInt(cmbCaixaComanda.getSelectedItem().toString().split(" - ")[0]));
        c.setStatus(cmbStatusComanda.getSelectedItem().toString());
        c.setFormaPgto(cmbPgtoComanda.getSelectedItem().toString());
        
        String gorjetaStr = txtGorjetaComanda.getText().replace(",", ".");
        c.setGorjeta(Double.parseDouble(gorjetaStr));

        emanuellynogueiratrizotti.dao.ComandaDAO dao = new emanuellynogueiratrizotti.dao.ComandaDAO();
        dao.alterar(c);
        
        // Lógica de transição e liberação de mesas
        emanuellynogueiratrizotti.dao.MesaDAO mesaDao = new emanuellynogueiratrizotti.dao.MesaDAO();
        
        // Regra 1: Se o cliente trocou de mesa, libera a antiga imediatamente
        if (c.getIdMesa() != idMesaAntiga) {
            mesaDao.atualizarStatus(idMesaAntiga, "Disponível");
            
            // Se a comanda continua aberta, ocupa a mesa nova escolhida
            if (c.getStatus().equals("ABERTA")) {
                mesaDao.atualizarStatus(c.getIdMesa(), "Ocupada");
            }
        }

        // Regra 2: Liberação da mesa atual se a comanda foi FECHADA
        if (c.getStatus().equals("FECHADA")) {
            mesaDao.atualizarStatus(c.getIdMesa(), "Disponível");
        }

        // Atualização da interface
        limparCamposComandas();
        atualizarTudo(); // Isso vai chamar o preencherCombosEstrangeiros e resetar a tela
        
        javax.swing.JOptionPane.showMessageDialog(this, "Comanda atualizada com sucesso!");
        
    } catch (HeadlessException | NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Erro: Verifique se todos os campos obrigatórios estão preenchidos corretamente.");
    }
    }//GEN-LAST:event_jButton14ActionPerformed

