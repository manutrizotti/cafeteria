    private void carregarTabelaComandas() {
        emanuellynogueiratrizotti.dao.ComandaDAO dao = new emanuellynogueiratrizotti.dao.ComandaDAO();
        java.util.List<emanuellynogueiratrizotti.model.Comanda> lista = dao.listarTodas();

        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblComandas.getModel();
        modelo.setRowCount(0);

        for (emanuellynogueiratrizotti.model.Comanda c : lista) {
            modelo.addRow(new Object[]{
                c.getIdComanda(),          // Coluna 0
                c.getIdMesa(),             // Coluna 1
                c.getStatus(),             // Coluna 2
                c.getFormaPgto(),          // Coluna 3
                c.getGorjeta(),            // Coluna 4
                c.getIdFuncionarioGarcom(),// Coluna 5 (dados extras para o clique usar)
                c.getIdFuncionarioCaixa()  // Coluna 6
            });
        }

        int abertas = dao.contarAbertas();
        lblComandasAbertas.setText("Comandas Abertas: " + abertas);
    }
    
    private void preencherCombosEstrangeiros() {
        // Limpa tudo antes de preencher para não duplicar se chamar de novo
        cmbMesaComanda.removeAllItems();
        cmbGarcomComanda.removeAllItems();
        cmbCaixaComanda.removeAllItems();
        
        // Adiciona a opção padrão no topo
        cmbMesaComanda.addItem("Selecione");
        cmbGarcomComanda.addItem("Selecione");
        cmbCaixaComanda.addItem("Selecione");

        // 1. Preenche as Mesas (Busca no banco APENAS as Disponíveis)
        emanuellynogueiratrizotti.dao.MesaDAO mesaDao = new emanuellynogueiratrizotti.dao.MesaDAO();
        java.util.List<emanuellynogueiratrizotti.model.Mesa> mesas = mesaDao.listarPorStatus("Disponível");
        for (emanuellynogueiratrizotti.model.Mesa m : mesas) {
            // Guarda o ID e o Número juntos visualmente (Ex: "1 - Mesa 5")
            cmbMesaComanda.addItem(m.getIdMesa() + " - Mesa " + m.getNumero());
        }

        // 2. Preenche Garçons e Caixas
        emanuellynogueiratrizotti.dao.FuncionarioDAO funcDao = new emanuellynogueiratrizotti.dao.FuncionarioDAO();
        java.util.List<emanuellynogueiratrizotti.model.Funcionario> funcionarios = funcDao.listarTodos();
        
        for (emanuellynogueiratrizotti.model.Funcionario f : funcionarios) {
            if (f.getCargo().equals("Garçom")) {
                cmbGarcomComanda.addItem(f.getIdFuncionario() + " - " + f.getNome());
            } else if (f.getCargo().equals("Caixa")) {
                cmbCaixaComanda.addItem(f.getIdFuncionario() + " - " + f.getNome());
            }
        }
    }
    
    public void atualizarTudo() {
    preencherCombosEstrangeiros();
    carregarTabelaComandas();
    carregarTabelaMesas();
    carregarTabela();
    carregarTabelaFuncionarios();
}
