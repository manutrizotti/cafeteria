    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
int linhaSelecionada = tblProdutos.getSelectedRow();
    
    if (linhaSelecionada == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Selecione um produto na tabela para excluir!");
        return;
    }
    
    // A trava de confirmação entra aqui:
    int op = javax.swing.JOptionPane.showConfirmDialog(this, "Deseja realmente excluir este produto?", "Confirmação", javax.swing.JOptionPane.YES_NO_OPTION);
    
    // Se o usuário clicar em "Sim" (YES_OPTION), o código entra no IF e exclui
    if (op == javax.swing.JOptionPane.YES_OPTION) {
        // Pega o ID que está na primeira coluna (índice 0) da linha selecionada
        int id = (int) tblProdutos.getValueAt(linhaSelecionada, 0);
        
        ProdutoDAO dao = new ProdutoDAO();
        dao.remover(id);
        
        atualizarTudo();
        
        carregarTabela(); // Atualiza a tabela
        limparCampos();   // Limpa os campos
        javax.swing.JOptionPane.showMessageDialog(this, "Produto excluído com sucesso!");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

